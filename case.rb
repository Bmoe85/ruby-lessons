puts "What is your favorite color?"
fav_color = gets.chomp

case fav_color
when "red"
	puts "Red like fire!"
when "blue"
	puts "Blue like the sky"
when "orange"
	puts "Orange like a harvest moon"
when "yellow"
	puts "Yellow like the bright sun"
when "pink"
	puts "Pink like a baby girl's blanket"
when "black"
	puts "Black like the universe"
else
	puts "Well i like blue so just pick that one."
end

puts "What is the weather like where you live?"
weather = gets.chomp

case weather
when "sunny"
	puts "It's flip flop time! Throw on some shorts and get out there."
when "raining"
	puts "Uhhgg, no fair. Pants and rain boots. Wet toes are not cute."
when "cloudy"
	puts "Well, it seems fine but bring a jacket just incase things turn south."
when "windy"
	puts "Pants and a warm top. No one wants to Marilyn Monroe at work!"
else	
	puts "I dont know. Do I look like a weather man to you! Wear whatever you want."
end	
	
	

	
